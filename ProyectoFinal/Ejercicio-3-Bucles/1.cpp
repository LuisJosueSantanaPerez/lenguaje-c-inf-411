#include <iostream>
using namespace std;

int main() {
    // Bucle for
    for (int i = 1; i <= 5; i++) {
        cout << i << endl;
    }

    // Bucle while
    int contador = 5;
    while (contador > 0) {
        cout << contador << endl;
        contador--;
    }

    return 0;
}
